public class MenuTree {
    private String title;
    private String key;
    private List<MenuTree> children;

    public MenuTree() {
    }

    public MenuTree(String title, String key) {
        this.title = title;
        this.key = key;
    }

    public MenuTree(String title, String key, List<MenuTree> children) {
        this.title = title;
        this.key = key;
        this.children = children;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public List<MenuTree> getChildren() {
        return children;
    }

    public void setChildren(List<MenuTree> children) {
        this.children = children;
    }

    @Override
    public String toString() {
        return "MenuTree{" +
                "title='" + title + '\'' +
                ", key='" + key + '\'' +
                ", children=" + children +
                '}';
    }
}
